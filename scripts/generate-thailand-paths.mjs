// One-time build script: converts public/th.json (province GeoJSON) into
// pre-projected, simplified SVG path strings saved as a TS module.
// Run with:  node scripts/generate-thailand-paths.mjs
import { readFileSync, writeFileSync } from "fs";

// Match ThailandMap.tsx projection constants exactly.
const LON0 = 97.0, LAT1 = 21.0, SCALE = 22, XS = SCALE * 0.974;
const VW = (106.4 - LON0) * XS;
const VH = (LAT1 - 5.5) * SCALE;
const project = (lon, lat) => [(lon - LON0) * XS, (LAT1 - lat) * SCALE];

// Douglas-Peucker line simplification (works on projected [x,y] points).
function perpendicularDist([x, y], [x1, y1], [x2, y2]) {
  const dx = x2 - x1, dy = y2 - y1;
  const len = Math.hypot(dx, dy);
  if (len === 0) return Math.hypot(x - x1, y - y1);
  return Math.abs(dy * x - dx * y + x2 * y1 - y2 * x1) / len;
}
function simplify(pts, eps) {
  if (pts.length <= 2) return pts;
  let maxD = 0, idx = 0;
  for (let i = 1; i < pts.length - 1; i++) {
    const d = perpendicularDist(pts[i], pts[0], pts[pts.length - 1]);
    if (d > maxD) { maxD = d; idx = i; }
  }
  if (maxD >= eps) {
    const l = simplify(pts.slice(0, idx + 1), eps);
    const r = simplify(pts.slice(idx), eps);
    return [...l.slice(0, -1), ...r];
  }
  return [pts[0], pts[pts.length - 1]];
}

// Convert a GeoJSON ring (lon/lat pairs) to a projected, simplified SVG path fragment.
const EPS = 0.6; // SVG units; ~0.85 screen px at 280px render width
function ringToPath(ring) {
  const projected = ring.map(([lon, lat]) => project(lon, lat));
  const simplified = simplify(projected, EPS);
  if (simplified.length < 3) return "";
  return "M " + simplified.map(([x, y]) => `${x.toFixed(1)},${y.toFixed(1)}`).join(" L ") + " Z";
}

// Arithmetic centroid of a lon/lat ring (good enough for province-sized polygons).
function ringCentroid(ring) {
  const n = ring.length;
  let lon = 0, lat = 0;
  for (const [lo, la] of ring) { lon += lo; lat += la; }
  return [lon / n, lat / n];
}

const geojson = JSON.parse(readFileSync("data/th.json", "utf8"));

const paths = geojson.features.map((f) => {
  const { geometry, properties } = f;
  let d = "";
  let centroid = [0, 0];

  if (geometry.type === "Polygon") {
    d = geometry.coordinates.map(ringToPath).join(" ");
    centroid = ringCentroid(geometry.coordinates[0]);
  } else if (geometry.type === "MultiPolygon") {
    // Use the largest polygon's outer ring for the centroid
    const rings = geometry.coordinates.map(poly => poly[0]);
    const largest = rings.reduce((a, b) => b.length > a.length ? b : a);
    d = geometry.coordinates.flat(1).map(ringToPath).join(" ");
    centroid = ringCentroid(largest);
  }

  const [cx, cy] = project(centroid[0], centroid[1]);
  return { id: properties.id, name: properties.name, d, cx: +cx.toFixed(2), cy: +cy.toFixed(2) };
}).filter((p) => p.d);

// Stats
const totalIn = geojson.features.reduce((s, f) => {
  const rings = f.geometry.type === "Polygon"
    ? f.geometry.coordinates
    : f.geometry.coordinates.flat(1);
  return s + rings.reduce((r, ring) => r + ring.length, 0);
}, 0);
const totalOut = paths.reduce((s, p) => s + (p.d.match(/,/g) || []).length, 0);
console.log(`Provinces: ${paths.length}  |  Points: ${totalIn} → ~${totalOut}  |  VW=${VW.toFixed(1)} VH=${VH.toFixed(1)}`);

const out = `// AUTO-GENERATED by scripts/generate-thailand-paths.mjs — do not edit by hand.
// Source: public/th.json (simplemaps.com province boundaries)
// Projection: equirectangular, LON0=97, LAT1=21, SCALE=22, XS=SCALE*0.974
// ViewBox: 0 0 ${VW.toFixed(1)} ${VH.toFixed(1)}

export const VW = ${VW.toFixed(1)};
export const VH = ${VH.toFixed(1)};

export type ProvincePath = { id: string; name: string; d: string; cx: number; cy: number };

export const PROVINCE_PATHS: ProvincePath[] = ${JSON.stringify(paths, null, 2)};
`;

writeFileSync("app/components/thailand-paths.ts", out);
console.log("Written: app/components/thailand-paths.ts");
